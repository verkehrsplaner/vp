        int s = JOptionPane.showOptionDialog(null, "Möchtest du den Bahnhof wirklich löschen?", "Spiel Beenden",
                JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE,
                null, options, options[0]);
        if (s == 0) {
            bhf.letzterSchritt();
            strg.bhfEntfernen(bhf);
            dispose();
        }


